#include<bits/stdc++.h>
using namespace std;
int main(){
        // for loop
        for (int i = 0; i<10; i++){
                cout<<i<<" ";

        }
        cout<<endl;
        // while loop
        int j = 0;
        while (j<10){
                cout<<j<<" ";
                j++;
        }
        cout<<endl;
        // do while loop
        int k = 0;
        do 
        {
                cout<<k<<" ";
                k++;
        }while (k!=10);
        
        return 0;

}